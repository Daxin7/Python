### 
# python tiene varios tipos de datos, existen datos escalares 
# que son aquellos que represetan un solo valor y no tiene subestructuras internas
###

###
# ejemplo: int, float, str, bool,
#   Nonetype, list, tuple, dict, range, set ....
###

# int(enteros)
print("***int: ****")
print(type(10)) # -> imprime en consola el tipo de dato
print(50)
print(60)
print(-5)
print(747474745 + 1)

print('\n')

# float(decimales)
print("***float: ****")
print(type(3.14))
print(5.3)
print(555.12)
print(1e3)
print(type(1 + 2j)) #-> del tipo complejo

print('\n')

# bool(booleanos)
print("***bool: ****")
print(type(True))
print(type(False))
print(1 > 2)

print('\n')

# valor que describe la ausencia de valor, valga la redundancia
print("***Nonetype: ****")
print(type(None))